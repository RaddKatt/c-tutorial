/*
Demonstrates some comparisons
*/

#include <stdio.h>

int main()
{
	printf("2 == 1: %d\n", 2 == 1);
	printf("1 == 1: %d\n", 1 == 1);
	printf("5 > 4: %d\n", 5 > 4);
	printf("5 < 4: %d\n", 5 < 4);
	printf("5 >= 5: %d\n", 5 >= 5);
	printf("5 <= 5: %d\n", 5 <= 5);
	printf("5 != 4: %d\n", 5 != 4);

	return 0;
}
